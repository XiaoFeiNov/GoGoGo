package main

import "fmt"

func main()  {
	m := 1 << 1 | 1 << 5 // 0010 0010，左移1位即1 * (2 ^ 1) = 2，左移5位即1 * (2 ^ 5) = 32，再对0000 0010和0010 0000进行或操作
	n := 1 << 1 | 1 << 2 // 0000 0110
	fmt.Printf("m = %08b n = %08b/n", m, n) // %08b至少打印8个字符宽度，不足用0填充
	fmt.Printf("m ^ n = %08b/n", m ^ n) // 0010 0100，m异或n，加不进位，减不借位
	fmt.Printf("m &^ n = %08b/n", m &^ n) // 0010 0000，m&^n，左右相同则清零，不同则按取左边
}